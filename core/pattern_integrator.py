# core/pattern_integrator.py

from patterns.pattern_manager import pattern_manager
from core.market_engine import market_engine
from config.settings import PATTERN_CONFIG


def detect_advanced_patterns(
        symbol: str,
        timeframe: str = "1d",
        limit: int = 200,
        exchange: str = None,
        pattern_types: list = None,
        use_cache: bool = True
):
    """
    Erkennt komplexe Chart-Patterns wie Double Bottom, Head & Shoulders etc.
    Nutzt market_engine f√ºr OHLCV-Daten und pattern_manager f√ºr Analyse.

    Args:
        symbol: Trading Pair (z.B. "BTC/USDT")
        timeframe: Zeitrahmen (z.B. "1d", "4h")
        limit: Anzahl der Candles f√ºr Analyse
        exchange: Exchange-Name oder None f√ºr Auto-Routing
        pattern_types: Spezifische Pattern-Typen oder None f√ºr alle
        use_cache: Cache f√ºr wiederholte Analysen nutzen

    Returns:
        Dict mit erkannten Patterns nach Typ gruppiert
    """

    # ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ 1. OHLCV-Daten von market_engine holen ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ #
    df = market_engine.get_ohlcv(symbol, timeframe, limit, exchange)

    if df.empty:
        print(f"‚ùå Keine Daten f√ºr {symbol} verf√ºgbar")
        return {}

    # ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ 2. Sicherstellen, dass die Daten korrekt formatiert sind ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ #
    # PatternManager erwartet m√∂glicherweise eine 'date' Spalte statt 'datetime'
    if 'datetime' in df.columns and 'date' not in df.columns:
        df['date'] = df['datetime']

    # ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ 3. Pattern-Typen festlegen ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ #
    if pattern_types is None:
        # Alle in settings.py definierten Patterns verwenden
        pattern_types = PATTERN_CONFIG.get('advanced_patterns', [])

    # ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ 4. PatternManager aufrufen ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ #
    patterns = pattern_manager.detect_patterns(
        df=df,
        timeframe=timeframe,
        pattern_types=pattern_types,
        use_cache=use_cache,
        state=None  # State ist optional, aktuell nicht implementiert
    )

    # ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ 5. Statistiken ausgeben ‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢ #
    stats = pattern_manager.get_pattern_count(patterns)
    print(f"üìä Pattern-Analyse f√ºr {symbol} ({timeframe}):")
    print(f"   - {stats['total']} Patterns gefunden")
    print(f"   - {stats['bullish']} bullish, {stats['bearish']} bearish, {stats['neutral']} neutral")

    return patterns